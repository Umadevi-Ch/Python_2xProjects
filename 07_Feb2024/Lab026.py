# Assignment Operator
name = "Pramod"
# It will store the value of variable literal to the identifier

# Unary Operator - single value or single lateral
Age = 95
print(Age)
my_bank_bal = -100
print(my_bank_bal)

# Not Operator
is_married = True
print(not is_married)
# 'not' will do the reverse of true condition

# is Operator - Identity Operator

a = 5
b = 5
c = False
print(a is b)
print(a is c)

my_list = [1,2,3]
my_list2 = [1,2,3]
print(my_list is my_list2)



